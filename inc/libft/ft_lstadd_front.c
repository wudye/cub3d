#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	t_list	*tp;

	if (!*lst)
		*lst = new;
	else
	{
		tp = *lst;
		*lst = new;
		new->next = tp;
	}
}
